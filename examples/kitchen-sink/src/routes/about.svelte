<h1 class="text-3xl font-bold">About</h1>
<p class="mt-2 text-slate-600">
    This kitchen-sink demo shows every public belte feature in one app: nested layouts and resolve
    hooks, standalone JSON API routes, page+api colocation (data loaders and form actions),
    cookie-session auth with a protected page, WebSockets on the same Bun.serve process, and the
    framework's default cache-control + error responses.
</p>
<p class="mt-4 text-slate-600">
    The stack is Bun (runtime, server, file-system router, bundler), Svelte 5 (components + SSR),
    and Tailwind v4. No Node, no Vite, no second process.
</p>
<p class="mt-4 text-sm text-slate-500">
    Source: <code class="font-mono">examples/kitchen-sink/src/routes/</code>
</p>
